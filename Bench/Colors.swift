import UIKit

struct Colors {
    static func green() -> UIColor {
       return UIColor(red:0.15, green:0.68, blue:0.38, alpha:1)
    }
    
    static func grey() -> UIColor {
        return UIColor(red:0.51, green:0.51, blue:0.51, alpha:1)
    }
    
    static func lightGrey() -> UIColor {
        return UIColor(red:0.66, green:0.66, blue:0.73, alpha:1)
    }
    
    static func white() -> UIColor {
        return UIColor.whiteColor()
    }
}
